/**
 * Creates 1-800 Ads Stripe products + one-time prices for each tier.
 * Run: node --env-file=.env scripts/create-stripe-products.mjs
 */
import Stripe from 'stripe'
import { writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const TIERS = [
  { adCount: 5, total: 249 },
  { adCount: 10, total: 399 },
  { adCount: 20, total: 699 },
  { adCount: 50, total: 1499 },
  { adCount: 100, total: 2499 },
]

const PRODUCT_KEY = '1800-ads'

const secretKey = process.env.STRIPE_SECRET_KEY
if (!secretKey) {
  console.error('Missing STRIPE_SECRET_KEY. Run with: node --env-file=.env scripts/create-stripe-products.mjs')
  process.exit(1)
}

const stripe = new Stripe(secretKey, { apiVersion: '2024-06-20' })

async function findExistingProduct(adCount) {
  const { data } = await stripe.products.search({
    query: `active:'true' AND metadata['product']:'${PRODUCT_KEY}' AND metadata['adCount']:'${adCount}'`,
    limit: 1,
  })
  return data[0] ?? null
}

async function findExistingPrice(productId, unitAmount) {
  const { data } = await stripe.prices.list({
    product: productId,
    active: true,
    limit: 20,
  })

  return (
    data.find(
      (price) =>
        price.type === 'one_time' &&
        price.currency === 'usd' &&
        price.unit_amount === unitAmount,
    ) ?? null
  )
}

async function ensureTier({ adCount, total }) {
  const label = `${adCount} Static Ad${adCount === 1 ? '' : 's'}`
  const unitAmount = total * 100

  let product = await findExistingProduct(adCount)

  if (!product) {
    product = await stripe.products.create({
      name: `1-800 Ads — ${label}`,
      description: `${adCount} conversion-focused static ads · concept, copy, design, every aspect ratio`,
      metadata: {
        product: PRODUCT_KEY,
        adCount: String(adCount),
      },
    })
    console.log(`Created product ${product.id} (${label})`)
  } else {
    console.log(`Found product ${product.id} (${label})`)
  }

  let price = await findExistingPrice(product.id, unitAmount)

  if (!price) {
    price = await stripe.prices.create({
      product: product.id,
      currency: 'usd',
      unit_amount: unitAmount,
      metadata: {
        product: PRODUCT_KEY,
        adCount: String(adCount),
      },
    })
    console.log(`Created price ${price.id} ($${total})`)
  } else {
    console.log(`Found price ${price.id} ($${total})`)
  }

  return { adCount, productId: product.id, priceId: price.id, total }
}

async function main() {
  const results = []

  for (const tier of TIERS) {
    results.push(await ensureTier(tier))
  }

  const priceMap = Object.fromEntries(results.map(({ adCount, priceId }) => [adCount, priceId]))

  const outPath = join(dirname(fileURLToPath(import.meta.url)), '../src/data/stripe-price-ids.ts')
  const contents = `/** Generated by scripts/create-stripe-products.mjs — re-run script to refresh IDs */
import type { AdCountOption } from './1800-ads-pricing'

export const STRIPE_PRICE_IDS: Record<AdCountOption, string> = {
  5: '${priceMap[5]}',
  10: '${priceMap[10]}',
  20: '${priceMap[20]}',
  50: '${priceMap[50]}',
  100: '${priceMap[100]}',
}

export const STRIPE_PRODUCT_IDS: Record<AdCountOption, string> = {
  5: '${results.find((r) => r.adCount === 5)?.productId}',
  10: '${results.find((r) => r.adCount === 10)?.productId}',
  20: '${results.find((r) => r.adCount === 20)?.productId}',
  50: '${results.find((r) => r.adCount === 50)?.productId}',
  100: '${results.find((r) => r.adCount === 100)?.productId}',
}
`

  writeFileSync(outPath, contents)
  console.log(`\nWrote ${outPath}`)
  console.log('\nPrice IDs:')
  for (const { adCount, priceId, total } of results) {
    console.log(`  ${adCount} ads ($${total}): ${priceId}`)
  }
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
